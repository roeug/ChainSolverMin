C
C THE FOLLOWING ARE OPTIONAL CALLS WHICH THE USER MAY MAKE TO
C GAIN ADDITIONAL CAPABILITIES IN CONJUNCTION WITH LSOIBT.
C (THE ROUTINES XSETUN AND XSETF ARE DESIGNED TO CONFORM TO THE
