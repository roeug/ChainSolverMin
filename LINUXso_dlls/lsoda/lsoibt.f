 575    CONTINUE
      IWORK(16) = IMXER
C COMPUTE RESIDUAL IF RELEVANT. ----------------------------------------
 580  LYD0 = LYH + NYH
      DO 585 I = 1,N
         RWORK(I+LSAVR-1) = RWORK(I+LYD0-1)/H
 585     Y(I) = RWORK(I+LYH-1)
